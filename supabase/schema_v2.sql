-- BulkAF Schema v2: Rezepte, Nahrungsmittel, Meal-Prep-Rotation, Kalorien-Log
-- Im Supabase SQL Editor NACH schema.sql ausführen.

-- Nahrungsmittel: user_id = null bedeutet "global/Standard für alle"
create table if not exists foods (
  id uuid primary key default gen_random_uuid(),
  user_id uuid references auth.users(id) on delete cascade,
  name text not null,
  unit text not null check (unit in ('g','ml')),
  kcal numeric not null,
  protein numeric not null default 0,
  carbs numeric not null default 0,
  fat numeric not null default 0,
  price numeric,
  price_note text,
  created_at timestamptz default now()
);

alter table foods enable row level security;

create policy "Read own or global foods" on foods for select
  using (user_id is null or auth.uid() = user_id);
create policy "Insert own foods" on foods for insert
  with check (auth.uid() = user_id);
create policy "Update own foods" on foods for update
  using (auth.uid() = user_id) with check (auth.uid() = user_id);
create policy "Delete own foods" on foods for delete
  using (auth.uid() = user_id);

-- Rezepte: user_id = null bedeutet "globale Standard-Rezepte für alle User"
create table if not exists recipes (
  id uuid primary key default gen_random_uuid(),
  user_id uuid references auth.users(id) on delete cascade,
  name text not null,
  meal_type text not null check (meal_type in ('fruehstueck','mittag','nachmittag','abend')),
  created_at timestamptz default now()
);

alter table recipes enable row level security;

create policy "Read own or global recipes" on recipes for select
  using (user_id is null or auth.uid() = user_id);
create policy "Insert own recipes" on recipes for insert
  with check (auth.uid() = user_id);
create policy "Update own recipes" on recipes for update
  using (auth.uid() = user_id) with check (auth.uid() = user_id);
create policy "Delete own recipes" on recipes for delete
  using (auth.uid() = user_id);

-- Zutaten pro Rezept (Basis-Menge bei Referenz-Kalorien des Rezepts)
create table if not exists recipe_items (
  id uuid primary key default gen_random_uuid(),
  recipe_id uuid not null references recipes(id) on delete cascade,
  food_id uuid not null references foods(id) on delete cascade,
  amount numeric not null
);

alter table recipe_items enable row level security;

create policy "Read items of visible recipes" on recipe_items for select
  using (exists (
    select 1 from recipes r where r.id = recipe_id
    and (r.user_id is null or r.user_id = auth.uid())
  ));
create policy "Manage items of own recipes" on recipe_items for all
  using (exists (select 1 from recipes r where r.id = recipe_id and r.user_id = auth.uid()))
  with check (exists (select 1 from recipes r where r.id = recipe_id and r.user_id = auth.uid()));

-- Aktiver Meal-Plan pro User und Mahlzeiten-Slot (manuelle Rotation: "Rezept X für Y Tage")
create table if not exists active_meal_plan (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  meal_slot text not null check (meal_slot in ('fruehstueck','mittag','nachmittag','abend')),
  recipe_id uuid not null references recipes(id),
  started_at date not null default current_date,
  planned_until date,
  unique(user_id, meal_slot)
);

alter table active_meal_plan enable row level security;

create policy "Users manage own active meal plan" on active_meal_plan for all
  using (auth.uid() = user_id) with check (auth.uid() = user_id);

-- Kalorien-Log: welche Mahlzeit wurde an welchem Tag tatsächlich gegessen
create table if not exists meal_log (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  log_date date not null default current_date,
  meal_slot text not null check (meal_slot in ('fruehstueck','mittag','nachmittag','abend')),
  kcal numeric not null,
  eaten_at timestamptz default now(),
  unique(user_id, log_date, meal_slot)
);

alter table meal_log enable row level security;

create policy "Users manage own meal log" on meal_log for all
  using (auth.uid() = user_id) with check (auth.uid() = user_id);

